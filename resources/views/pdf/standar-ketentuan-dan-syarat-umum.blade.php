    <h1 style="font-size: 11pt; text-decoration: underline; text-align: center;">STANDAR KETENTUAN DAN SYARAT UMUM</h1>
    <span style="display: block; text-align: center; margin-top: 1px; font-weight: bold;">SURAT PERINTAH KERJA (SPK)</span>

    <ol style="margin-top: 20px; padding-left: 18px;">
        <li>
            <strong>LINGKUP PEKERJAAN</strong><br>
            Penyedia yang ditunjuk berkewajiban untuk menyelesikan pekerjaan dalam jangka waktu yang ditentukan, dengan mutu sesuai spesifikasi teknis dan harga sesuai SPK.
        </li>
        <li>
            <strong>HUKUM YANG BERLAKU</strong><br>
            Keabsahan, interpretasi, dan pelaksanaan SPK ini didasarkan kepada hukum Republik Indonesia.
        </li>
        <li>
            <strong>PENYEDIA JASA MANDIRI</strong><br>
            SPK ini tidak dimaksudkan untuk menciptakan hubungan hukum antara PKA dengan Penyedia seperti hubungan hukum antara majikan dan buruh atau antara principal dan agen. Penyedia bertanggung jawab penuh terhadap personilnya.
        </li>
        <li>
            <strong>HARGA SPK</strong>
            <ol type="a">
                <li>PKA membayar kepada penyedia atas pelaksanaan pekerjaan dalam SPK sebesar harga SPK.</li>
                <li>Harga SPK telah memperhitungkan keuntungan, beban pajak dan biaya overhead serta biaya asuransi yang meliputi juga biaya keselamatan dan kesehatan kerja.</li>
                <li>Rincian harga SPK sesuai dengan rincian yang tercantum dalam daftar kunatitas dan harga (untuk kontrak harga satuan atau kontrak gabungan harga satuan dan lumpsump).</li>
            </ol>
        </li>
        <li>
            <strong>HAK KEPEMILIKAN</strong><br>
            TPK berhak atas kepemilikan semua barang/bahan yang terkait langsung atau disediakan sehubungan dengan barang/jasa yang diberikan oleh Penyedia kepada PKA. Jika diminta oleh PKA maka Penyedia berkewajiban untuk membantu secara optimal pengalihan hak kepemilikan tersebut kepada TPK sesuai dengan hukum yang berlaku.<br>
            Hak kepemilikan atas peralatan dan barang/bahan yang disediakan oleh PKA tetap pada PKA, dan semua peralatan tersebut harus dikembalikan kepada PKA pada saat SPK berakhir atau jika tidak diperlukan lagi oleh Penyedia. Semua peralatan tersebut harus dikembalikan dalam keadaan dan kondisi yang sama pada saat diberikan kepada Penyedia dengan pengecualian keausan akibat pemakaian yang wajar.
        </li>
        <li>
            <strong>CACAT MUTU</strong><br>
            PKA akan memeriksa setiap hasil pekerjaan Penyedia dan memberitahukan secara tertulis kepada Penyedia atas setiap cacat mutu yang ditemukan. PKA dapat memerintahkan kepada Penyedia untuk menemukan dan mengungkapkan cacat mutu, serta menguji pekerjaan yang dianggap oleh PKA mengandung cacat mutu. Penyedia bertanggung jawab atas cacat mutu selama 6 (enam) bulan setelah serah terima hasil pekerjaan.
        </li>
        <li>
            <strong>PERPAJAKAN</strong><br>
            Penyedia berkewajiban untuk membayar pajak, bea, retribusi, dan pungutan lain yang dibebankan oleh hukum yang berlaku atas pelaksanaan SPK. Semua pengeluaran perpajakan ini dianggap telah termasuk dalam nilai SPK.
        </li>
        <li>
            <strong>PENGALIHAN DAN/ATAU SUBKONTRAK</strong><br>
            Penyedia dilarang untuk mengalihkan dan/atau mensubkontrakkan sebagian atau seluruh pekerjaan. Pengalihan seluruh pekerjaan hanya diperbolehkan dalam hal pergantian nama Penyedia, baik sebagai akibat peleburan (merger) atau akibat lainnya.
        </li>
        <li>
            <strong>JADWAL</strong>
            <ol type="a">
                <li>SPK ini berlaku efektif pada tanggal penandatangan oleh para pihak.</li>
                <li>Waktu pelaksanaan SPK adalah sejak tanggal mulai kerja.</li>
                <li>Penyedia harus menyelesaikan pekerjaan sesuai jadwal yang ditentukan.</li>
                <li>Apabila penyedia berpendapat tidak dapat menyelesaikan pekerjaan sesuai jadwal karena keadaan diluar pengendaliannya dan penyedia telah melaporkan kejadian tersebut kepada PKA, maka PKA dapat melakukan penjadwalan kembali pelaksanaan tugas penyedia dengan adendum SPK.</li>
            </ol>
        </li>
        <li>
            <strong>ASURANSI</strong>
            <ol type="a">
                <li>
                    Penyedia wajib menyediakan asuransi sejak SPK ditandatangani sampai dengan tanggal selesainya pemeliharaan untuk:
                    <ol type="1">
                        <li>Semua barang dan peralatan yang mempunyai risiko tinggi terjadinya kecelakaan, pelaksanaan pekerjaan, serta pekerja untuk pelaksanaan pekerjaan, atas segala risiko terhadap kecelakaan, kerusakan, kehilangan, serta risiko lain yang tidak dapat diduga;</li>
                        <li>Pihak ketiga sebagai akibat kecelakaan ditempat kerjanya;</li>
                        <li>Perlindungan terhadap kegagalan bangunan.</li>
                    </ol>
                </li>
                <li>Besarnya asuransi sudah diperhitungkan dalam penawaran dan termasuk dalam harga SPK.</li>
            </ol>
        </li>
        <li>
            <strong>PENANGGUNGAN DAN RISIKO</strong>
            <ol type="a">
                <li>
                    Penyedia berkewajiban untuk melindungi, membebaskan dan menanggung tanpa batas PKA beserta instansinya terhadap semua bentuk tuntutan, tanggung jawab, kewajiban, kehilangan, kerugian, denda, gugatan atau tuntutan hukum, proses pemeriksaan hukum, dan biaya yang dikenakan terhadap PKA beserta instansinya (kecuali kerugian yang mendasari tuntutan tersebut disebabkan kesalahan atau kelalaian berat PKA) sehubungan dengan klaim yang timbul dari hal-hal berikut terhitung sejak Tanggal Mulai Kerja sampai dengan tanggal penandatanganan berita cara penyerahan akhir:
                    <ol type="1">
                        <li>Kehilangan atau kerusakan peralatan dan harta benda penyedia, dan personil</li>
                        <li>Cidera tubuh, sakit atau kematian personil;</li>
                        <li>Kehilangan atau keruskan harta benda, dan cidera tubuh, sakit atau kematian pihak ketiga.</li>
                    </ol>
                </li>
                <li>
                    Terhitung sejak Tanggal Mulai Kerja sampai dengan tanggal penandatanganan berita acara penyerahan awal, semua risiko kehilangan atau kerusakan Hasil pekerjaan ini, Bahan dan perlengkapan merupakan risiko penyedia, kecuali kerugian atau kerusakan tersebut diakibatkan oleh kesalahan atau kelalaian PKA.
                </li>
                <li>
                    Pertanggungan asuransi yang dimiliki oleh penyedia tidak membatasi kewajiban penanggungan dalam syarat ini.
                </li>
                <li>
                    Kehilangan atau kerusakan terhadap Hasil Pekerjaan atau Bahan yang menyatu dengan Hasil Pekerjaan selama Tanggal Mulai Kerja dan batas akhir masa pemeliharaan harus diganti atau diperbaiki oleh penyedia atas tanggungannya sendiri jika kehilangan atau keruskan tersebut terjadi akibat tindakan atau kelalaian penyedia.
                </li>
            </ol>
        </li>
        <li>
            <strong>PENGAWASAN DAN PEMERIKSAAN</strong><br>
            TPK berwenang melakukan pengawasan dan pemeriksaan terhadap pelaksanaan pekerjaan yang dilaksanakan oleh penyedia. Apabila diperlukan PKA dapat memerintahkan kepada pihak ketiga untuk melakukan pengawasan dan pemeriksaan atas semua pelaksanaan pekerjaan yang dilaksanakan oleh penyedia.
        </li>
        <li>
            <strong>PENGUJIAN</strong><br>
            Jika PKA atau pengawas pekerjaan memerintahkan penyedia untuk melakukan pengujian Cacat Mutu yang tidak tercantum dalam spesifikasi Teknis dan Gambar, dan hasil uji coba menunjukkan adanya cacat mutu maka penyedia berkewajiban untuk menanggung biaya pengujian tersebut. Jika tidak ditemukan adanya cacat mutu maka uji coba tersebut dianggap sebagai peristiwa kompensasi.
        </li>
        <li>
            <strong>LAPORAN HASIL PEKERJAAN</strong><br>
            Pemeriksaan pekerjaan dilakukan setelah pelaksanaan SPK untuk menetapkan volume pekerjaan atau kegiatan yang telah dilaksanakan guna pembayaran hasil pekerjaan. Hasil pemeriksaan pekerjaan dituangkan dalam berita acara penerimaan hasil pekerjaan.
        </li>
        <li>
            <strong>WAKTU PENYELESAIAN PEKERJAAN</strong>
            <ol type="a">
                <li>Kecuali SPK diputuskan lebih awal, penyedia berkewajiban untuk memulai pelaksanaan pekerjaan atau kegiatan yang telah dilaksanakan guna pembayaran hasil pekerjaan. Hasil pemeriksaan pekerjaan dituangkan dalam berita acara penerimaan hasil pekerjaan.</li>
                <li>Jika pekerjaan tidak selesai pada Tanggal Penyelesaian bukan akibat Keadaan Kahar atau Peristiwa Kompensasi atau karena kesalahan atau kelalaian penyedia maka penyedia akan dikenakan denda.</li>
                <li>Jika keterlambatan tersebut semata-mata disebabkan oleh Peristiwa Kompensasi maka PKA dikenakan kewajiban pembayaran ganti rugi. Denda atau ganti rugi tidak dikenakan jika Tanggal penyelesaian disepakati oleh Para Pihak untuk diperpanjang.</li>
                <li>Tanggal Penyelesaian yang dimaksud dalam ketentuan ini adalah tanggal penyelesaian semua pekerjaan.</li>
            </ol>
        </li>
        <li>
            <strong>SERAH TERIMA PEKERJAAN</strong>
            <ol type="a">
                <li>Setelah pekerjaan selesai 100% (seratus perseratus), penyedia mengajukan permintaan secara tertulis kepada PKA untuk penyerahan pekerjaan.</li>
                <li>Dalam rangka penilaian hasil pekerjaan PKA menugaskan TPK.</li>
                <li>Pejabat Penerima Hasil Pekerjaan melakukan penilaian terhadap hasil pekerjaaan yang telah diselesaikan oleh penyedia. Apabila terdapat kekurangan-kekurangan dan/atau cacat hasil pekerjaan, penyedia wajib memperbaiki/menyelesaikannya, atas perintah PKA.</li>
            </ol>
        </li>
        <li>
            <strong>PERUBAHAN SPK</strong>
            <ol type="a">
                <li>SPK hanya dapat diubah melalui adendum SPK.</li>
                <li>
                    Perubahan SPK bisa dilaksanakan apabila disetujui oleh para pihak, meliputi:
                    <ol type="1">
                        <li>Perubahan pekerjaan disebabkan oleh sesuatu hal yang dilakukan oleh para pihak dalam SPK sehingga mengubah lingkup pekerjaan dalam SPK;</li>
                        <li>Perubahan jadwal pelaksanaan pekerjaan akibat adanya perubahan pekerjaan;</li>
                        <li>Perubahan harga SPK akibat adanya perubahan pekerjaan dan/atau perubahan pelaksanaan pekerjaan.</li>
                    </ol>
                </li>
                <li>Untuk kepentingan perubahan SPK, PKA dapat membentuk Pejabat Peneliti Pelaksanaan SPK.</li>
            </ol>
        </li>
        <li>
            <strong>PERISTIWA KOMPENSASI</strong>
            <ol type="a">
                <li>
                    Peristiwa Kompensasi dapat diberikan kepada penyedia dalam hal sebagai berikut:
                    <ol type="1">
                        <li>PKA mengubah jadwal yang dapat mempengaruhi pelaksanaan pekerjaan;</li>
                        <li>Keterlambatan pembayaran kepada penyedia;</li>
                        <li>PKA tidak memberikan gambar-gambar, spesifikasi dan/atau instruksi sesuai jadwal yang dibutuhkan;</li>
                        <li>Penyedia belum bisa masuk ke lokasi sesuai jadwal;</li>
                        <li>PKA menginstruksikan kepada pihak penyedia untuk melakukan pengujian tambahan yang setelah dilaksanakan pengujian ternyata tidak ditemukan kerusakan/kegagalan/penyimpangan;</li>
                        <li>PKA memerintahkan penundaan pelaksanaan pekerjaan;</li>
                        <li>PKA memerintahkan untuk mengatasi kondisi tertentu yang tidak dapat diduga sebelumnya dan disebabkan oleh PKA;</li>
                        <li>Ketentuan lain dalam SPK.</li>
                    </ol>
                </li>
                <li>Jika Peristiwa Kompensasi mengakibatkan pengeluaran tambahan dan/atau keterlambatan penyelesaian pekerjaan maka PKA berkewajiban untuk membayar ganti rugi dan/atau memberikan perpanjangnan waktu penyelesaian pekerjaan.</li>
                <li>Ganti rugi hanya dapat dibayarkan jika berdasarkan data penunjang dan perhitungan kompensasi yang diajukan oleh penyedia kepada PKA, dapat dibuktikan kerugian nyata akibat Peristiwa Kompensasi.</li>
                <li>Perpanjagan waktu penyelesaian pekerjaan hanya dapat diberikan jika berdasarkan data penunjang dan perhitungan kompensasi yang diajukan oleh penyedia kepada PKA, dapat dibuktikan perlunya tambahan waktu akibat Peristiwa Kompensasi.</li>
                <li>Penyedia tidak berhak atas ganti rugi dan/atau perpanjangan waktu penyelesaian pekerjaan jika penyedia gagal atau lalai untuk memberikan peringatan dini dalam mengantisipasi atau mengatasi dampak Peristiwa Kompensasi.</li>
            </ol>
        </li>
        <li>
            <strong>PERPANJANGAN WAKTU</strong>
            <ol type="a">
                <li>Jika terjadi Peristiwa Kompensasi sehingga penyelesaian pekerjaan akan melampui Tanggal Penyelesaian maka penyedia berhak untuk meminta perpanjangan Tanggal Penyelesaian berdasarkan data penunjang. PKA berdasarkan data penunjang dan pertimbangan Pengawas Pekerjaan memperpanjang Tanggal Penyelesaian Pekerjaan secara tertulis. Perpanjangan Tanggal Penyelesaian harus dilakukan melalui adendum SPK jika perpanjangan tersebut mengubah Masa SPK.</li>
                <li>PKA dapat menyetujui perpanjangan waktu pelaksanaan setelah melakukan penelitian terhadap usulan tertulis yang diajukan oleh penyedia.</li>
            </ol>
        </li>
        <li>
            <strong>PENGHENTIAN DAN PEMUTUSAN SPK</strong>
            <ol type="a">
                <li>Penghentian SPK dapat dilakukan karena pekerjaan sudah selesai atau terjadi keadaan Kahar.</li>
                <li>
                    Dalam hal SPK dihentikan, maka PKA wajib membayar kepada penyedia sesuai dengan prestasi pekerjaan yang telah dicapai, termasuk:
                    <ol type="1">
                        <li>Biaya langsung pengadaan bahan dan perlengkapan ini harus diserahkan oleh penyedia kepada PKA, dan selanjutnya menjadi hak milik PKA;</li>
                        <li>Biaya langsung pembongkaran dan demobilisasi hasil pekerjaan sementara dan peralatan;</li>
                        <li>Biaya langsung demobilisasi personil.</li>
                    </ol>
                </li>
                <li>Pemutusan SPK dapat dilakukan oleh pihak penyedia atau pihak PKA.</li>
                <li>
                    Menyimpang dari Pasal 1266 dan 1267 Kitab Undang-Undang Hukum Perdata, Pemutusan SPK melalui pemberitahuan tertulis dapat dilakukan apabila:
                    <ol type="1">
                        <li>Penyedia lalai/cidera janji dalam melaksanakan kewajibannya dan tidak memperbaiki kelalaiannya dalam jangka waktu yang telah ditetapkan;</li>
                        <li>Penyedia tanpa persetujuan Pengawas Pekerjaan, tidak memulai pelaksanaan pekerjaan;</li>
                        <li>Penyedia menghentikan pekerjaan selama 28 (dua puluh delapan) hari dan penghentian ini tidak tercantum dalam program mutu serta tanpa persetujuan Pengawas Pekerjaan;</li>
                        <li>Penyedia berada dalam keadaan pailit;</li>
                        <li>Penyedia selama Masa SPK gagal memperbaiki Cacat Mutu dalam jangka waktu yang ditetapkan oleh PKA;</li>
                        <li>Denda keterlambatan pelaksanaan pekerjaan akibat kesalahan penyedia sudah melampui 5% (lima perseratus) dari harga SPK dan PKA menilai bahwa penyedia tidak akan sanggup menyelesaikan sisa pekerjaan;</li>
                        <li>Pengawas pekerjaan memerintahkan penyedia untuk menunda pelaksanaan atau kelanjutan pekerjaan, dan perintah tersebut tidak ditarik selama 28 (dua puluh delapan) hari;</li>
                        <li>TPK tidak menerbitkan SPP untuk pembayaran tagihan angsuran sesuai dengan disepakati sebagaimana tercantum dalam SPK.</li>
                        <li>Penyedia terbukti melakukan KKN, kecurangan dan/atau pemalsuan dalam proses pengadaan yang diputuskan oleh instansi yang berwenang; dan/atau</li>
                        <li>Pengaduan tentang penyimpangan prosedur, dugaan KKN dan/atau pelanggaran persaingan sehat dalam pelaksanaan pengadaan dinyatakan benar oleh instansi yang berwenang.</li>
                    </ol>
                </li>
                <li>
                    Dalam hal pemutusan SPK dilakukan karena kesalahan penyedia:
                    <ol type="1">
                        <li>Penyedia membayar denda; dan/atau</li>
                        <li>Penyedia dimasukkan dalam Daftar Hitam.</li>
                    </ol>
                </li>
                <li>
                    Dalam hal pemutusan SPK dilakukan karena PKA terlibat penyimpangan prosedur, melakukan KKN dan/atau pelanggaran persaingan sehat dalam pelaksanaan pengadaan, maka PKA dikenakan sanksi berdasarkan peraturan perundang-undangan.
                </li>
            </ol>
        </li>
        <li>
            <strong>PEMBAYARAN</strong>
            <ol type="a">
                <li>
                    Pembayaran prestasi hasil pekerjaan yang disepakati dilakukan dengan ketentuan:
                    <ol type="1">
                        <li>Penyedia telah mengajukan tagihan disertai laporan kemajuan hasil pekerjaan;</li>
                        <li>Pembayaran dilakukan dengan [sistem bulanan/sistem termin/pembayaran secara sekaligus];</li>
                        <li>Pembayaran dilakukan senilai pekerjaan yang telah terpasang, tidak termasuk bahan/material dan peralatan yang ada dilokasi pekerjaan;</li>
                        <li>Pembayaran harus dipotong denda (apabila ada), pajak dan uang retensi.</li>
                    </ol>
                </li>
                <li>Pembayaran terakhir hanya dilakukan setelah pekerjaan selesai 100% (seratus perseratus) dan Berita Acara penyerahan pertama pekerjaan diterbitkan.</li>
                <li>TPK dalam kurun waktu 7 (tujuh) hari kerja setelah pengajuan permintaan pembayaran dari penyedia harus sudah mengajukan surat permintaan pembayaran kepada PKA.</li>
                <li>Bila terdapat ketidak sesuaian dalam perhitungan angsuran, tidak akan menjadi alasan untuk menunda pembayaran, PKA dapat meminta penyedia untuk menyampaikan perhitungan prestasi sementara dengan mengesampingkan hal-hal yang sedang menjadi perselisihan.</li>
            </ol>
        </li>
        <li>
            <strong>DENDA</strong><br>
            Penyedia berkewajiban untuk membayar sanksi finansial berupa denda sebagai akibat wanprestasi atau cidera janji terhadap kewajiban-kewajiban penyedia dalam SPK ini. PKA mengenakan denda dengan memotong angsuran pembayaran prestasi pekerjaan penyedia. Pembayaran denda tidak mengurangi tanggung jawab kontraktual penyedia.
        </li>
        <li>
            <strong>PENYELESAIAN PERSELISIHAN</strong><br>
            TPK dan penyedia berkewajiban untuk berupaya sungguh-sungguh menyelesaikan secara damai semua perselisihan yang timbul dari atau berhubungan dengan SPK ini atau interpretasinya selama atau setelah pelaksanaan pekerjaan. Jika perselisihan tidak dapat diselesaikan secara musyawarah maka perselisihan akan diselesaikan melalui pengadilan negeri dalam wilayah hukum Republik Indonesia.
        </li>
        <li>
            <strong>LARANGAN PEMBERIAN KOMISI</strong><br>
            Penyedia menjamin bahwa tidak satu pun personil Desa telah atau akan menerima komisi atau keuntungan tidak sah lainnya baik langsung maupun tidak langsung dari SPK ini. Penyedia menyetujui bahwa pelanggaran syarat ini merupakan pelanggaran yang mendasar terhadap SPK ini.
        </li>
    </ol>
